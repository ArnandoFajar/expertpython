# Upper Case
a = "Hello, World!"
print(a.upper())

print("\n")

# Lower Case
b = "Hello, World!"
print(b.lower())

print("\n")

# Remove Whitespace
a = " Hello, World! "
print(a.strip())  # returns "Hello, World!"

print("\n")

# Replace String
a = "Hello, World!"
print(a.replace("H", "J"))

# Split String
a = "Hello, World!"
print(a.split(","))  # returns ['Hello', ' World!']
